
package com.mcwbalance.settings;

/**
 * Used to store hard limits
 *
 * @author Alex
 */
public class Limit {

    /**
     * Maximum allowed size of a DAC
     */
    public static final int MAX_DAC_SIZE = 1000;
    /**
     * Maximum allowed number of deposition Rates
     */
    public static final int MAX_DEPO_RATES = 50;
    /**
     * Max model duration set to 365000 time steps
     */
    public static final int MAX_DURATION = 365 * 1000;
    /**
     * Max number of nodes
     */
    public static final int MAX_NODES = 20;
    /**
     * Max number of operating levels
     */
    public static final int MAX_LEVELS = 20;
    /**
     * Max number of land cover types
     */
    public static final int MAX_LAND_COVERS = 15;
    /**
     * Max number of states
     */
    public static final int MAX_STATES = 10;
    /**
     * Max number of transfers between nodes
     */
    public static final int MAX_TRNS = 40;

}
